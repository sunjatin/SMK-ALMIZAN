                          <h4>Description</h4>
                          <p>perbankan syariah di Indonesia kini tengah berkembang pesat seiring dengan maju dan meningkatnya kepercayaan umat muslim untuk menggunakan jasa atau layanan syariah sebagai benttuk transaksinya. Tidak hanya bidang perbankan saja, sistem syariah juga berkembang merambah industri lainnya seperti perhotelan, usaha Mikro,kecil dan menengah (UMKM). Banyaknya sektor industri tersebut menjadi sebuah peluang dan tantangan akan kebutuhan sumber daya yang mumpuni</p>
                          <p>Hadirnya Program Keahlian Perbankan Syariah merupakan salah satu jawaban, SMK dengan jurusan perbankan syariah. Lulusan SMK jurusan perbankan syariah bisa diarahkan bukan sekedar melahirkan orang pandai, tetapi juga memiliki integritas dan keimanan yang tangguh. Plus mampu bekerja dalam bidang perbankan syariah seperti tenaga akuntan syariah pada bidang operasional lembaga keuangan syariah, manajemen perbankan syariah, dan pemasaran syariah</p>
                          <p>Apakah Jurusan Perbankan Syariah SMK itu?</p>
                          <p>Jurusan perbankan syariah bisa dibilang jurusan yang baru di SMK dan masih dalam hitungan jari SMK yang membuka jurusan ini. Dengan keahlian lulusan yang dimiliki, peluang kerja pun lumayan banyak. Lihat saja industri- industri berbasis sistem syariah yang saat ini berkembang dan melingkupi segala bidang</p>
                          <p>Materi Pembelajaran Jurusan Perbankan Syariah</p>
                          <p>Perbankan Syariah merupakan salah satu paket keahlian rumpun bidang keahlian Bisnis dan Manajemen dalam sub program keahlian Akuntansi dan Keuangan. Selain mata pelajaran umum seperti mapel pada Muatan Nasional,&nbsp;Muatan Kewilayahan, dan muatan dalam dasar bidang keahlian.&nbsp; Untuk siswa kelas sepuluh, pada dasar program keahlian akan mempelajari berbagai macam pelajaran&nbsp; &nbsp;mencakup etika profesi, aplikasi pengolah angka, akuntansi dasar, dan perbankan dasar. Sedangkan kelas sebelas, dan dua belas, siswa akan belajar mengenai&nbsp;Pengelolaan Kas, Ekonomi Islam,&nbsp;Layanan Lembaga Keuangan Syariah, Akuntansi Perbankan Syariah,&nbsp;Komputer Akuntansi, serta&nbsp; Produk Kreatif dan Kewirausahaan.&nbsp;</p>
                          <blockquote>
                            <p>Melihat besarnya pasar lembaga-lembaga menggunakan sistem syariah yang makin menggurita, lulusan jurusan perbankan syariah memiliki peluang yang lumayan luas. Mereka bisa mengisi bidang pekerjaan seperi teller, bagian administrasi pada lembaga keuangan seperti bank, operator mesin hitung, dan sebagainya. Akan tetapi, lebih baik lagi kalau lulusan perbankan syariah bisa melanjutkan ke tingkat perguruan tinggi untuk memperdalam kompetensi dan karirnya.&nbsp;</p>
                          </blockquote>
                        
